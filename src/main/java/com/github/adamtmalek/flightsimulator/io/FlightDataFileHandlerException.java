package com.github.adamtmalek.flightsimulator.io;

public class FlightDataFileHandlerException extends Exception {
	public FlightDataFileHandlerException(String message) {
		super(message);
	}

	public FlightDataFileHandlerException(Throwable cause) {
		super(cause);
	}

	public FlightDataFileHandlerException(String message, Throwable cause) {
		super(message, cause);
	}
}
